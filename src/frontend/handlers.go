func renderHTTPError(log logrus.FieldLogger, r *http.Request, w http.ResponseWriter, err error, code int) {
	log.WithField("error", err).Error("request error")
	errMsg := fmt.Sprintf("%+v", err)

	w.WriteHeader(code)

	if templateErr := templates.ExecuteTemplate(w, "error", injectCommonTemplateData(r, map[string]interface{}{
		"error":       errMsg,
		"status_code": code,
		"status":      http.StatusText(code),
	})); templateErr != nil {
		log.Println(templateErr)
	}
}

func injectCommonTemplateData(r *http.Request, payload map[string]interface{}) map[string]interface{} {
	data := map[string]interface{}{
		"session_id":        sessionID(r),
		"request_id":        r.Context().Value(ctxKeyRequestID{}),
		"user_currency":     currentCurrency(r),
		"platform_css":      plat.css,
		"platform_name":     plat.provider,
		"is_cymbal_brand":   isCymbalBrand,
		"assistant_enabled": assistantEnabled,
		"deploymentDetails": deploymentDetailsMap,
		"frontendMessage":   frontendMessage,
		"currentYear":       time.Now().Year(),
		"baseUrl":           baseUrl,
	}

	for k, v := range payload {
		data[k] = v
	}

	return data
}

func currentCurrency(r *http.Request) string {
	c, _ := r.Cookie(cookieCurrency)
	if c != nil {
		return c.Value
	}
	return defaultCurrency
}

func sessionID(r *http.Request) string {
	v := r.Context().Value(ctxKeySessionID{})
	if v != nil {
		return v.(string)
	}
	return ""
}

func cartIDs(c []*pb.CartItem) []string {
	out := make([]string, len(c))
	for i, v := range c {
		out[i] = v.GetProductId()
	}
	return out
}

// get total # of items in cart
func cartSize(c []*pb.CartItem) int {
	cartSize := 0
	for _, item := range c {
		cartSize += int(item.GetQuantity())
	}
	return cartSize
}

func renderMoney(money pb.Money) string {
	currencyLogo := renderCurrencyLogo(money.GetCurrencyCode())
	return fmt.Sprintf("%s%d.%02d", currencyLogo, money.GetUnits(), money.GetNanos()/10000000)
}

func renderCurrencyLogo(currencyCode string) string {
	logos := map[string]string{
		"USD": "$",
		"CAD": "$",
		"JPY": "¥",
		"EUR": "€",
		"TRY": "₺",
		"GBP": "£",
	}

	logo := "$" //default
	if val, ok := logos[currencyCode]; ok {
		logo = val
	}
	return logo
}

// starRating converts a rating value to star characters.
func starRating(rating interface{}) string {
	var r float64
	switch v := rating.(type) {
	case float64:
		r = v
	case int:
		r = float64(v)
	default:
		return ""
	}
	full := int(r + 0.5) // round to nearest
	return strings.Repeat("★", full) + strings.Repeat("☆", 5-full)
}

func stringinSlice(slice []string, val string) bool {
	for _, item := range slice {
		if item == val {
			return true
		}
	}
	return false
}

// getProductReviews fetches reviews and stats for a product from the review service.
// Errors are non-fatal — reviews are secondary content.
func (fe *frontendServer) getProductReviews(ctx context.Context, productID string) ([]*Review, *ReviewStats) {
	log := ctx.Value(ctxKeyLog{}).(logrus.FieldLogger)

	if fe.reviewSvcAddr == "" {
		return nil, nil
	}

	// Fetch reviews list with retry
	reviews := &ReviewsResponse{}
	reviewsURL := "http://" + fe.reviewSvcAddr + "/reviews?product_id=" + productID
	reviewsRes, err := retryGet(ctx, reviewsURL)
	if err != nil {
		log.WithError(err).Warn("failed to fetch reviews after retries")
		return nil, nil
	}
	defer reviewsRes.Body.Close()
	if err := json.NewDecoder(reviewsRes.Body).Decode(reviews); err != nil {
		log.WithError(err).Warn("failed to decode reviews response")
		return nil, nil
	}

	log.WithFields(logrus.Fields{
		"product_id":             productID,
		"reviews_count_from_svc": len(reviews.Reviews),
	}).Info("getProductReviews: fetched reviews list")

	// Fetch review stats with retry
	stats := &ReviewStats{}
	statsURL := "http://" + fe.reviewSvcAddr + "/reviews/stats?product_id=" + productID
	statsRes, err := retryGet(ctx, statsURL)
	if err != nil {
		log.WithError(err).Warn("failed to fetch review stats after retries")
		return reviews.Reviews, nil
	}
	defer statsRes.Body.Close()
	if err := json.NewDecoder(statsRes.Body).Decode(stats); err != nil {
		log.WithError(err).Warn("failed to decode review stats response")
		return reviews.Reviews, nil
	}

	log.WithFields(logrus.Fields{
		"product_id":    productID,
		"total_reviews": stats.TotalReviews,
		"avg_rating":    stats.AverageRating,
	}).Info("getProductReviews: fetched stats")

	return reviews.Reviews, stats
}

func (fe *frontendServer) postReviewHandler(w http.ResponseWriter, r *http.Request) {
	log := r.Context().Value(ctxKeyLog{}).(logrus.FieldLogger)

	if fe.reviewSvcAddr == "" {
		renderHTTPError(log, r, w, errors.New("review service not configured"), http.StatusServiceUnavailable)
		return
	}

	body, err := io.ReadAll(r.Body)
	if err != nil {
		renderHTTPError(log, r, w, errors.Wrap(err, "failed to read request body"), http.StatusBadRequest)
		return
	}

	url := "http://" + fe.reviewSvcAddr + "/reviews"
	proxyReq, err := http.NewRequestWithContext(r.Context(), http.MethodPost, url, bytes.NewReader(body))
	if err != nil {
		renderHTTPError(log, r, w, errors.Wrap(err, "failed to create proxy request"), http.StatusInternalServerError)
		return
	}
	proxyReq.Header.Set("Content-Type", "application/json")

	res, err := http.DefaultClient.Do(proxyReq)
	if err != nil {
		renderHTTPError(log, r, w, errors.Wrap(err, "failed to create review"), http.StatusInternalServerError)
		return
	}
	defer res.Body.Close()

	w.WriteHeader(res.StatusCode)
	io.Copy(w, res.Body)
}

func (fe *frontendServer) getReviewsHandler(w http.ResponseWriter, r *http.Request) {
	log := r.Context().Value(ctxKeyLog{}).(logrus.FieldLogger)
	productID := mux.Vars(r)["product_id"]
	if productID == "" {
		renderHTTPError(log, r, w, errors.New("product_id is required"), http.StatusBadRequest)
		return
	}

	reviews, stats := fe.getProductReviews(r.Context(), productID)

	reviewCount := 0
	if reviews != nil {
		reviewCount = len(reviews)
	}

	var buf bytes.Buffer
	if err := templates.ExecuteTemplate(&buf, "reviews", injectCommonTemplateData(r, map[string]interface{}{
		"reviews":      reviews,
		"review_stats": stats,
		"product_id":   productID,
	})); err != nil {
		log.Println(err)
		return
	}

	log.WithFields(logrus.Fields{
		"product_id":    productID,
		"review_count":  reviewCount,
		"has_stats":     stats != nil,
		"response_len":  buf.Len(),
	}).Info("getReviewsHandler rendered")

	w.Header().Set("Content-Type", "text/html; charset=utf-8")
	w.Header().Set("Content-Length", strconv.Itoa(buf.Len()))
	w.Header().Set("Connection", "close")
	w.WriteHeader(http.StatusOK)
	if _, err := io.Copy(w, &buf); err != nil {
		log.WithError(err).Warn("getReviewsHandler: write failed")
	}
	if flusher, ok := w.(http.Flusher); ok {
		flusher.Flush()
	}
}
